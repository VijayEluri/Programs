	public String[] getSortedList(String[] kings) {
		Arrays.sort(kings, new Comparator<String>() {
			@Override
			public int compare(String o1, String o2) {
				String[] s1 = o1.split(" "), s2 = o2.split(" ");
				final int diff1 = s1[0].compareTo(s2[0]);
				if(diff1 == 0) {
					final int diff2 = getRomanValue(s1[1]) - getRomanValue(s2[1]);
					return diff2;
				}
				return diff1;
			}
		});
		return kings;
	}

	int getValue(char c) {
		switch (c) {
		case 'X':
			return 10;
		case 'V':
			return 5;
		case 'L':
			return 50;
		case 'I':
			return 1;
		default:
			return -1;
		}
	}

	int getRomanValue(String roman) {
		final char[] cs = roman.toCharArray();
		char prev = cs[0];
		int value = getValue(prev);
		for (int i = 1; i < cs.length; ++i) {
			final char c = cs[i];
			final int val = getValue(c);
			if (val <= getValue(prev)) {
				value += val;
			} else {
				value = val - value;
			}
			prev = c;
		}
		return value;
	}

